package entity;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Review {
    private String id;
    private String username;
    private String review;
    private Date date;

    public Review() {
    }

    public Review(String id, String username, String review, Date date) {
        this.id = id;
        this.username = username;
        this.review = review;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
}
